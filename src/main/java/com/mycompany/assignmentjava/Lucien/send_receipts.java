      private void loadTransactions() {
        // Retrieve all transaction records
        List<String> records = FileManager.getAllRecords(FileManager.FileType.TRANSACTIONS);

        // Get the table model
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows (if any)

        // For each record in transactions.txt
        for (String record : records) {
            String[] parts = record.split(FileManager.DELIMITER);
            // We expect at least 7 parts: transactionID, orderID, customerID, amount, paymentDate, paymentStatus, receiptGenerated
            if (parts.length >= 7) {
                model.addRow(new Object[]{
                    parts[0], // transactionID
                    parts[1], // orderID
                    parts[2], // customerID
                    parts[3], // amount
                    parts[4], // paymentDate
                    parts[5], // paymentStatus
                    parts[6]  // receiptGenerated
                });
            }
        }
    }
